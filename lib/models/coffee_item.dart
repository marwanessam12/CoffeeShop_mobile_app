class CoffeeItem {
  final String title;
  final String subtitle;
  final String image;
  final double price;
  final double rating;
  final int reviews;
  final String description;

  CoffeeItem({
    required this.title,
    required this.subtitle,
    required this.image,
    required this.price,
    required this.rating,
    required this.reviews,
    required this.description,
  });
}
